#!/usr/bin/env bash
# build-fragment.sh — extract the actual `hooks{}` block from this machine's
# `~/.claude/settings.json` and write it to stdout as a redistributable
# fragment, ready to be saved as `settings/hooks.fragment.json`.
#
# The pack ships with an EMPTY fragment (all 5 lifecycle events = null) by
# design — that's a safety placeholder, not data loss. The real payload
# belongs on the SOURCE machine, which is the only place that knows what
# was actually wired. Run this script there.
#
# Usage (on the SOURCE machine):
#   bash bin/build-fragment.sh > settings/hooks.fragment.json
#
# Then `git add` + commit the result so install.sh on any other machine has
# something real to merge.
#
# Exit codes:
#   0 — fragment written to stdout successfully
#   1 — no settings.json found, or no `hooks` key in it
#   2 — unexpected JSON shape

set -euo pipefail

SRC="${HOME}/.claude/settings.json"
if [[ ! -f "$SRC" ]]; then
  echo "[build-fragment.sh] ERROR: $SRC does not exist — run this on the SOURCE machine that has the wiring." >&2
  exit 1
fi

python3 - "$SRC" <<'PYEOF'
import json, sys, pathlib

src = pathlib.Path(sys.argv[1])
try:
    raw = json.loads(src.read_text())
except json.JSONDecodeError as e:
    print(f"[build-fragment.sh] ERROR: $SRC is not valid JSON: {e}", file=sys.stderr)
    sys.exit(2)

if not isinstance(raw, dict):
    print(f"[build-fragment.sh] ERROR: $SRC is not a JSON object at the top level.", file=sys.stderr)
    sys.exit(2)

hooks = raw.get("hooks")
if not isinstance(hooks, dict) or not hooks:
    print(f"[build-fragment.sh] ERROR: $SRC has no `hooks` key, or it is empty.", file=sys.stderr)
    sys.exit(1)

# Keep only the 5 lifecycle events Claude Code recognises. Drop anything else
# (settings, permissions, env, etc.) — those are machine-local and shouldn't
# ride along in a redistributable fragment.
events = ["SubagentStart", "UserPromptSubmit", "PreToolUse", "SessionStart", "Stop"]
out = {"_comment": "Generated by bin/build-fragment.sh from this machine's ~/.claude/settings.json. Merge the top-level event keys into ~/.claude/settings.json on the target machine via install.sh."}
total = 0
for ev in events:
    if ev in hooks and hooks[ev] is not None:
        out[ev] = hooks[ev]
        total += len(hooks[ev]) if isinstance(hooks[ev], list) else 1
    else:
        out[ev] = None

print(json.dumps(out, indent=2, ensure_ascii=False))
print(f"[build-fragment.sh] wrote {total} hook entries across {sum(1 for ev in events if out.get(ev))} events", file=sys.stderr)
PYEOF
